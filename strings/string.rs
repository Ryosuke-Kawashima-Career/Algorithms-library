fn is_lower(c: char) -> bool {
    let dif_from_a: u8 = (c as u8) - ('a' as u8);
    return dif_from_a < 26;
}
fn is_upper(c: char) -> bool {
    let dif_from_a: u8 = (c as u8) - ('a' as u8);
    return dif_from_a >= 26;
}

